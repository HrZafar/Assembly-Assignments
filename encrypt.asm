.MODEL SMALL
.STACK 100h
.386
.DATA
	 key BYTE 123
	 str1 BYTE "abc123xyz$"
.CODE
  	MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		MOV CX,LENGTHOF str1
		DEC CX
		MOV SI,0
		MOV AL,KEY
		L1:
			XOR BYTE PTR str1[SI],AL
			INC SI
			LOOP L1
		MOV AH,9
		MOV DX,OFFSET str1
		INT 21h
		MOV AH,2
		MOV DL,10
		INT 21h
		MOV DL,13
		INT 21h
		MOV CX,LENGTHOF str1
		DEC CX
		MOV SI,0
		MOV AL,KEY
		L2:
			XOR BYTE PTR str1[SI],AL
			INC SI
			LOOP L2
		MOV AH,9
		MOV DX,OFFSET str1
		INT 21h
		MOV AH,4Ch
    	INT 21h
  	MAIN ENDP
END MAIN